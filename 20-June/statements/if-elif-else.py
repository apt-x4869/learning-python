
x = 100

# Simple if statement
if x >= 100 :
    print("X is greater than 100")


# If-else statement
if x < 100:
    print("x is less than 100")
else:
    print("x is greater")


# If-elif statement
if (x < 100):
    print("x is less than 100")
elif(x > 100):
    print("x is greater")
else:
    print("x is equal to 100")